/* *** Variable types
=> in general two types. primitive and non-primitive

Primitive types:
|-- Integer : byte, short, int, long
|-- Float : float, double
|-- Char
|-- Boolean

Integer:
--------
int --> 4 bytes -> 
long --> 8 bytes -> use l at last of long number. long longNumber = 34235l;
short --> 2 bytes ->
byte --> 2 bytes -> -2^7 to 2^7-1 that is -128 to 127 like int num = 25;

Float:
-------
float --> 4 bytes ->
double --> 8 bytes ->
Note: by deafault java uses double precision. double num = 5.6; but we can not do that float num = 5.6; because we have to explicitly define we want to use float, so add F or f to last of the value. num = 5.6f; now this is float value.

Char:
-----
char --> 2 bytes. java supports unicode. all types of characters supported.
char mychar = 'k'; use single quote.

Boolean: 
-------
boolean -> true or false. not 1 or 0
boolean flag = true; 



 */