public class Relational {
    public static void main(String[] args) {
        // <, >, <=, >=, ==, !=
        int x = 5;
        int y = 6;
        System.out.println(x<y); // true
        System.out.println(x>y); // false
        System.out.println(x==y); // false
        System.out.println(x!=y); // true

        double m = 5.6;
        double n = 6.6;
        System.out.println(m < n ); // true

        System.out.println(10.1 != 10.1);

    }
}
